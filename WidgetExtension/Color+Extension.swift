import Foundation
import SwiftUI
import DesignSystem

extension Color {
    static let b1: Color = Color(UIColor.b1)
    static let b2: Color = Color(UIColor.b2)
    static let b3: Color = Color(UIColor.b3)
}

extension Image {
    static let water: Image = Image("icoWater")
    static let steps: Image = Image("icoSteps")
    static let calories: Image = Image("icoCalories")
    static let sleep: Image = Image("icoSleep")
}
